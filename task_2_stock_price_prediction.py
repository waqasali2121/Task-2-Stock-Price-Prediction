# -----------------------------
# Load Dataset
# -----------------------------
df = yf.download('AAPL', start='2015-01-01', end='2024-01-01')

# -----------------------------
# Prepare data for Linear Regression
# -----------------------------
df_lr = df[['Open', 'High', 'Low', 'Close', 'Volume']].copy()

# Target = next day close
df_lr['Target'] = df_lr['Close'].shift(-1)
df_lr = df_lr.dropna()

X_lr = df_lr[['Open', 'High', 'Low', 'Close', 'Volume']]
y_lr = df_lr['Target']

# Train-test split (no shuffle)
split = int(len(X_lr) * 0.8)

X_train_lr = X_lr[:split]
X_test_lr = X_lr[split:]

y_train_lr = y_lr[:split]
y_test_lr = y_lr[split:]

# -----------------------------
# Train Linear Regression
# -----------------------------
lr_model = LinearRegression()
lr_model.fit(X_train_lr, y_train_lr)

# Predict
y_pred_lr = lr_model.predict(X_test_lr)

# Evaluate
mae_lr = mean_absolute_error(y_test_lr, y_pred_lr)
rmse_lr = np.sqrt(mean_squared_error(y_test_lr, y_pred_lr))

