import yfinance as yf
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

from sklearn.preprocessing import MinMaxScaler
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense

# -----------------------------
# 2. Load Dataset
# -----------------------------
# Download Apple stock data
df = yf.download('AAPL', start='2015-01-01', end='2024-01-01')

# Keep only Close price (most important for prediction)
data = df[['Close']]

# -----------------------------
# 3. Normalize Data (VERY IMPORTANT for LSTM)
# -----------------------------
scaler = MinMaxScaler(feature_range=(0,1))
scaled_data = scaler.fit_transform(data)

# -----------------------------
# 4. Create Sequences (Time Windows)
# -----------------------------
# Use last 60 days to predict next day
X = []
y = []

for i in range(60, len(scaled_data)):
    X.append(scaled_data[i-60:i])  # last 60 days
    y.append(scaled_data[i])       # next day

X, y = np.array(X), np.array(y)

# -----------------------------
# 5. Train-Test Split
# -----------------------------
train_size = int(len(X) * 0.8)

X_train = X[:train_size]
X_test = X[train_size:]

y_train = y[:train_size]
y_test = y[train_size:]

# -----------------------------
# 6. Build LSTM Model
# -----------------------------
model = Sequential()

# First LSTM layer
model.add(LSTM(units=50, return_sequences=True, input_shape=(X.shape[1], 1)))

# Second LSTM layer
model.add(LSTM(units=50))

# Output layer
model.add(Dense(1))

# Compile model
model.compile(optimizer='adam', loss='mean_squared_error')

# -----------------------------
# 7. Train Model
# -----------------------------
model.fit(X_train, y_train, epochs=10, batch_size=32)

# -----------------------------
# 8. Predictions
# -----------------------------
predictions = model.predict(X_test)

# Convert back to original price scale
predictions = scaler.inverse_transform(predictions)
y_test_actual = scaler.inverse_transform(y_test)

# -----------------------------
# 9. Plot Results
# -----------------------------
plt.figure(figsize=(10,5))

plt.plot(y_test_actual, label="Actual Price")
plt.plot(predictions, label="Predicted Price")

plt.title("Stock Price Prediction using LSTM")
plt.xlabel("Time")
plt.ylabel("Price")
plt.legend()

plt.show()

# -----------------------------
# 10. Predict Future Price
# -----------------------------
last_60_days = scaled_data[-60:]
last_60_days = last_60_days.reshape(1, 60, 1)

future_price = model.predict(last_60_days)
future_price = scaler.inverse_transform(future_price)

print("Predicted Next Day Price:", future_price[0][0])

